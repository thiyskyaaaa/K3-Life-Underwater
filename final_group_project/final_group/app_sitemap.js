const galleryDiv = document.querySelector(".gallery");
const gallerySVG = document.querySelector("#Galleryrect");

gallerySVG.addEventListener("mouseenter", () =>{
    galleryDiv.style.display = "flex";
});

gallerySVG.addEventListener("mouseleave", () =>{
    galleryDiv.style.display = "none";
});

